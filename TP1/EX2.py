# Cette fonction retourne le maximum des éléments d'un tuple donné
def max_tuple(t):
    # Utilisation de la fonction intégrée `max` pour trouver l'élément maximal du tuple
    return max(t)

# Exemple d'utilisation de la fonction avec un tuple d'entiers
print(max_tuple((1, 5, 3)))  # Affiche le maximum du tuple